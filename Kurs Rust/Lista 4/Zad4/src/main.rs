fn my_first_interpreter(code: &str) -> String {
    let mut state: u8 = 0;
    let mut res = String::new();
    for x in code.chars(){
        if x == '+'{
            if state == 255{
                state = 0;
            }
            else{
                state += 1;
            }
        }
        else if x == '.'{
            res.push(state as char)
        }
    }
    return res;
}

